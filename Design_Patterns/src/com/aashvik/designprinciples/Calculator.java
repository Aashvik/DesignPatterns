package com.aashvik.designprinciples;

public class Calculator {
	public float Addition(float x,float y)
	{
		return x+y;
	}
	public float Subtraction(float x,float y)
	{
		return x-y;
	}
	public float Multiplication(float x,float y)
	{
		return x*y;
	}
	public float Division(float x,float y)
	{
		return x/y;
	}
	public float Addition(float x,float y,float z)
	{
		return x+y+z;
	}
	public float Subtraction(float x,float y,float z)
	{
		return x-y-z;
	}
	public float Multiplication(float x,float y,float z)
	{
		return x*y*z;
	}
	public float Division(float x,float y,float z)
	{
		return (x/y)/z;
	}
}
